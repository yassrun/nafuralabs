/** App-wide Lucide icons (single provider — child components must not replace with a partial set). */
export function provideAppLucideIcons(): EnvironmentProviders {
  return makeEnvironmentProviders([
    {
      provide: LUCIDE_ICONS,
      useValue: new LucideIconProvider(APP_LUCIDE_ICONS),
    },
  ]);
}
